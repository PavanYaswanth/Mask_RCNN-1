top=tk.Tk()
top.geometry('800x600')
top.title('Image Segmentation')
top.configure(background='white')
label=Label(top,background='white', font=('arial',15,'bold'))
segment_image = Label(top)

def segment(file_path):
    global label_packed
    image = skimage.io.imread(file_path)
    # Run detection
    results = model.detect([image], verbose=1)

    # Visualize results
    r = results[0]
    visualize.display_instances(image, r['rois'], r['masks'], r['class_ids'], 
                            class_names, r['scores'])

def show_segment_button(file_path):
    segment_b=Button(top,text="Segment the Image",command=lambda: segment(file_path),padx=10,pady=5)
    segment_b.configure(background='#9400D3', foreground='white',font=('arial',10,'bold'))
    segment_b.place(relx=0.79,rely=0.46)

def upload_image():
    try:
        file_path=filedialog.askopenfilename()
        uploaded=Image.open(file_path)
        uploaded.thumbnail(((top.winfo_width()/2.25),(top.winfo_height()/2.25)))
        im=ImageTk.PhotoImage(uploaded)
        segment_image.configure(image=im)
        segment_image.image=im
        label.configure(text='')
        show_segment_button(file_path)
    except:
        pass

upload=Button(top,text="Upload an image",command=upload_image,padx=10,pady=5)
upload.configure(background='#9400d3', foreground='white',font=('arial',10,'bold'))
